package main

import "fmt"

func main() {
	fmt.Println("Muhammad")
	fmt.Println("Miraj")
	fmt.Println("Muhammad Miraj")
	fmt.Println(len("Muhammad Miraj")) // mengihitung jumlah huruf
	fmt.Println("Muhammad Miraj"[0])   //menghasilkan nilai byte dari huruf index ke 2
	fmt.Println("Muhammad Miraj"[1])   //menghasilkan nilai byte dari huruf index ke 1
}
